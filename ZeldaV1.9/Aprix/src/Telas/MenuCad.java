    public int codigoSeg() {
        Random sorteia = new Random();
        int valor = sorteia.nextInt(0, 4);
        int sorte;
        if (valor > 1) {
            sorte = sorteia.nextInt(48, 57);
        } else {
            sorte = sorteia.nextInt(97, 122);
        }
        return sorte;
    }

