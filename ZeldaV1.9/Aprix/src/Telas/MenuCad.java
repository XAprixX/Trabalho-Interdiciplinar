    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        //Primeiro definição de conta corrente ou poupança 
        if (definicaoConta == true) {
            Corrente = "corrente";
        } else {
            Corrente = "poupanca";
        }

        //Cliente alocando as infoermaçoes do cadastro nome,cpf,Data de nascimento e sua Senha 
        Cliente cliente = new Cliente();
        cliente.setNome(NomeCad.getText());
        cliente.setCPF(CpfCad.getText());
        cliente.setValor_conta(0);
        cliente.setConta(Corrente);
        cliente.setCodigo_segurança(codigoSeg());
        cliente.setData_nascimento(DDCad.getText());
        String senha = new String(SenhaCad.getPassword());

        if (NomeCad.getText().equalsIgnoreCase("") || CpfCad.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Informaçoes invalidas");
        } else {
            cadastroDAO.save(cliente);
            cadastroDAO.update(cliente);
            frame.trocarPainel(new MenuConta(frame, CpfCad.getText()));
        }
        try {
            cliente.setSenha_conta(Integer.parseInt(senha));

        } catch (NumberFormatException ex) {

        }

        //Salvando dentro do Banco de Dados
        //Atualiazando dentro do Banco de Dados 

    }//GEN-LAST:event_jButton1ActionPerformed

